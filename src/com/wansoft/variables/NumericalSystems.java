package com.wansoft.variables;

public class NumericalSystems {
	
	public static void main(String[] args) {
		int decimalNumber = 10;
		System.out.println("number in decimal system: " + decimalNumber);
		
		int octalNumber = 012;
		System.out.println("number in octal system: " + octalNumber);
		
		int hexaDecimalNumber = 0xA;
		System.out.println("number in hexadecimal system: " + hexaDecimalNumber);
		
		int binaryNumber = 0b1010;
		System.out.println("number in binary system: " + binaryNumber);
	}
}
