package com.wansoft.variables;

public class VariableRules {
	public static void main(String[] args) {
		/**
		 * start with lowercase
		 */
		String greeting = "Hello from JAVA";
		/**
		 * camel case
		 */
		String greetingFromJAva = "Hello";
		
		/**
		 * valid identifiers
		 */
		String _name;
		String $name;
		String name;
		String n123ame;
		String name4;
	}
}
